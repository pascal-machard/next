import numpy as np

def main():

    print("exemple 09 tuple 04 index")

    tuple_a = ("blue", "red", "green", "purple")
    print("tuple_a : ", tuple_a)

    print("----------------------------------")

    print("tuple_a[1] : ", tuple_a[1])
    print("tuple_a[-1] : ", tuple_a[-1])
    print("tuple_a[-2] : ", tuple_a[-2])

    print("tuple_a[2:4] : ", tuple_a[2:4])
    print("tuple_a[-3:] : ", tuple_a[-3:])
    print("tuple_a[-3:] : ", tuple_a[-3:-1])


if __name__ == "__main__":
    main()
